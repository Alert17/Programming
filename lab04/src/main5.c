//Знайти значення у-координати за графіком (Завдання 5)
int main()
{
	float x = 0;
	float y;
	////Значення х-координати брав з графіка, при різних значеннях виконувались різні рівняння
	if (x <= (-1)) {
		y = -x - 1;
	}
	if (x > (-1) && x <= 0) {
		y = x + 1;
	}
	if (x > 0 && x <= 1) {
		y = 1 - x;
	}
	if (x > 1) {
		y = x - 1;
	}
return 0;
}
